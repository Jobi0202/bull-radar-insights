export const getRecentMentions = (count: number = 10) => {
  return [...mockMentions]
    .sort((a, b) => new Date(b.Publish_date).getTime() - new Date(a.Publish_date).getTime())
    .slice(0, count);
};

export const getMentionsByChannel = (channelName: string, count: number = 10) => {
  return [...mockMentions]
    .filter(mention => mention.youtube_channel.toLowerCase().includes(channelName.toLowerCase()))
    .sort((a, b) => new Date(b.Publish_date).getTime() - new Date(a.Publish_date).getTime())
    .slice(0, count);
};

export const getMentionsByAsset = (assetName: string, count: number = 10) => {
  return [...mockMentions]
    .filter(mention => mention.Asset.toLowerCase().includes(assetName.toLowerCase()))
    .sort((a, b) => new Date(b.Publish_date).getTime() - new Date(a.Publish_date).getTime())
    .slice(0, count);
};

export const getUniqueChannels = () => {
  const channels = new Set(mockMentions.map(mention => mention.youtube_channel));
  return Array.from(channels);
};

export const getUniqueAssets = () => {
  const assets = new Set(mockMentions.map(mention => mention.Asset));
  return Array.from(assets);
};
